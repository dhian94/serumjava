    private void jbSaveEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveEditActionPerformed
        try {
            int row = getRowTable();
            String tipeRumahUpdate = (String) getTableValue(row, 2);
            System.out.println(tipeRumahUpdate);
            InputStream fis = new FileInputStream(imageFile);
            PreparedStatement statement = con.getKoneksi().prepareStatement("UPDATE `data_rumah` SET `tipe_rumah`=" + tipeRumahUpdate + ","
                    + "`gambar`=?,"
                    + "`nama_gambar`='" + jtfNamaGB.getText() + "',"
                    + "`tipe_rumah`='" + jtfTipeRumah.getText() + "',"
                    + "`luas_rumah/tanah`='" + jtfLuas.getText() + "',"
                    + "`deskripsi`='" + jTextArea1.getText() + "',"
                    + "`harga`='" + jtfHarga.getText() + "',"
                    + "`stok`='" + jtfStok.getText() + "'"
                    + " WHERE `data_rumah`.`tipe_rumah` = " + tipeRumahUpdate + ";");
            statement.setBlob(1, fis);
            // proses magic dimulai
            statement.executeUpdate();
            // tara!
            System.out.println("Edit success!");
            showData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Edit Gagal!" + e + "", "Maaf", 2);
        }
        tbEdit = false;
        tombolEdit();
        kosong();
    }//GEN-LAST:event_jbSaveEditActionPerformed

