    public void tombolEdit() {
        if (tbEdit == true) {
            jbEditRumah.setEnabled(false);
            jbDataUser.setEnabled(false);
            jbDeleteRumah.setEnabled(false);
            jbRefreshRumah.setEnabled(false);
            jbSave.setEnabled(false);
            jbSaveEdit.setVisible(true);
            tbEdit = false;
        } else if (tbEdit == false) {
            jbEditRumah.setEnabled(true);
            jbDataUser.setEnabled(true);
            jbDeleteRumah.setEnabled(true);
            jbRefreshRumah.setEnabled(true);
            jbSave.setEnabled(true);
            jbSaveEdit.setVisible(false);
        }
    }

