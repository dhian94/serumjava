    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        try {
            InputStream fis = new FileInputStream(imageFile);
            PreparedStatement statement = con.getKoneksi().prepareStatement("INSERT INTO `data_rumah`(`gambar`, "
                    + "`nama_gambar`, `tipe_rumah`, `luas_rumah/tanah`, "
                    + "`deskripsi`, `harga`, `stok`) VALUES "
                    + "(?,'"
                    + jtfNamaGB.getText() + "','"
                    + jtfTipeRumah.getText() + "','"
                    + jtfLuas.getText() + "','"
                    + jTextArea1.getText() + "','"
                    + jtfHarga.getText() + "','"
                    + jtfStok.getText() + "');");
            // mengisi data parameter pertama, yaitu gambar
            statement.setBlob(1, fis);
            // proses magic dimulai
            statement.executeUpdate();
            // tara!
            System.out.println("Insert success!");
//            imageFile = null;
            JOptionPane.showMessageDialog(this, "File anda sudah disimpan :)");
        } catch (Exception ex) {
            Logger.getLogger(JFileChooser.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "File gagal disimpan :(\n" + ex.getMessage());
        }
        jtfPilih.setText("");
        jtfNamaGB.setText("");
        jtfTipeRumah.setText("");
        jtfLuas.setText("");
        jTextArea1.setText("");
        jtfHarga.setText("");
        jtfStok.setText("");
        System.out.println("Sukses");
    }//GEN-LAST:event_jbSaveActionPerformed

