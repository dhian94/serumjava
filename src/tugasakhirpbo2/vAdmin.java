    private void jbBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBrowseActionPerformed
        // Hanya sastu file dalam satu file chooser
        jFileChooser1.setMultiSelectionEnabled(false);
        // Tipe dialog
        jFileChooser1.setDialogType(jFileChooser1.OPEN_DIALOG);
        // Ekstensi file yg dapat dipilih
        jFileChooser1.setFileFilter(new FileNameExtensionFilter("JPEG, JPG, PNG", new String[]{"JPEG", "JPG", "PNG"}));
        // Judul FileChooser
        jFileChooser1.setDialogTitle("Pilih Gambar yang dikehendaki");

        // Tampilkan file chooser, jika aksi dari user adalah OPEN FILE
        if (jFileChooser1.showOpenDialog(imagePreview) == jFileChooser1.OPEN_DIALOG) {
            // Mengambil file yang dipilih oleh user dalam FileChooser
            imageFile = jFileChooser1.getSelectedFile();
            // Menampilan path file kedalam jTextName
            jtfPilih.setText(imageFile.getAbsolutePath());
            // Jika file terlalu besar
            if (imageFile.length() > Integer.MAX_VALUE) {
                JOptionPane.showMessageDialog(this, "File terlalu besar!");
            }
        }
    }//GEN-LAST:event_jbBrowseActionPerformed

